
public class Data {
    private int nilai;
    private String deskripsi;

    public Data(int nilai, String deskripsi) {
        this.nilai = nilai;
        this.deskripsi = deskripsi;
    }

    public int getNilai() {
        return nilai;
    }

    public void setNilai(int nilai) {
        this.nilai = nilai;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }
}

